    /**
     * 检查客户的收货地址是否超出配送范围
     * @param address
     */
    private void checkOutOfRange(String address) throws Exception {
        log.info("检查配送范围 - 用户地址: {}, 店铺地址: {}", address, shopAddress);
        Map params = new LinkedHashMap<String, String>();
        params.put("output", "json");
        params.put("ak", ak);
        String shopCoordinate = GetCoordinate(Coordinate_URL, params, address);
        String userCoordinate = GetCoordinate(Coordinate_URL, params, shopAddress);
        params.put("origin",shopCoordinate);
        params.put("destination",userCoordinate);
        params.put("steps_info","0");

        //路线规划
        String json = HttpClientUtil.doGet(Direction_URL, params);

        JSONObject jsonObject = JSON.parseObject(json);
        if(!jsonObject.getString("status").equals("0")){
            throw new OrderBusinessException(MessageConstant.DIRECTION_ERROR);
        }

        //数据解析
        JSONObject result = jsonObject.getJSONObject("result");
        JSONArray jsonArray = (JSONArray) result.get("routes");
        Integer distance = (Integer) ((JSONObject) jsonArray.get(0)).get("distance");

        if(distance > 5000){
            //配送距离超过5000米
            throw new OrderBusinessException(MessageConstant.DIRECTION_OUT_ERROR);
        }
    }

    public String GetCoordinate(String strUrl, Map<String, String> param, String address) throws Exception {
        //获取地址的经纬度坐标
        param.put("address", address);
//        获取店铺经纬坐标
        String Coordinate = HttpClientUtil.doGet(strUrl, param);
        //店铺经纬度坐标
        Coordinate = parseAddress(Coordinate);

        return Coordinate;
    }
    public String parseAddress(String Coordinate){
        log.info("百度地图API返回结果: {}", Coordinate);
        JSONObject jsonObject = JSON.parseObject(Coordinate);
        String status = jsonObject.getString("status");
        if (!status.equals("0")){
            log.error("百度地图API调用失败, status: {}, message: {}", status, jsonObject.getString("message"));
            throw new OrderBusinessException(MessageConstant.ADDRESS_PARSE_ERROR);
        }
        JSONObject location = jsonObject.getJSONObject("result").getJSONObject("location");
        String lat = location.getString("lat");
        String lng = location.getString("lng");
        String result = lat + "," + lng;
        return result;
    }



